function mostrar()
{
	//Sintaxis: for(let inicialización de variable; condición; transformación de la variable)
	/*Cuándo elijo si programo con FOR o con WHILE?
	-Cuando CONOZCO de antemano (en tiempo de producción/de compilación) la cantidad d repeticiones, usamos FOR.
	-Cuando NO CONOCEMOS de antemano la cantidad de repeticiones, usamos WHILE.
	*/

	for (let i = 1; i < 11; i++){
		console.log(i);
	};
}